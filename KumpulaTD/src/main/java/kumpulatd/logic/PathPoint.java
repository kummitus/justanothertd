package kumpulatd.logic;

/**
 *
 * @author kummi
 */
public class PathPoint {
    private int x;
    private int y;
    
    public PathPoint(int x, int y){
        this.x = x;
        this.y = y;
    }
    
    public int getX(){
        return x;
    }
    
    public int getY(){
        return y;
    }
    
}
